          public async Task ExecuteAsync(HttpContext httpContext)
          {
               httpContext.Response.ContentType        = MediaTypeNames.Text.Html;                                                                              // retourne "text/html". Pour connaître les différents 'ContentType' -> Google : 'contenttype html'
